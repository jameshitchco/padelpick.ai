export function scoreRacket(racket, answers) {
  let score = 0;
  const { level, position, background, style, pace } = answers;

  if (racket.level.includes(level)) score += 30;
  if (racket.position.includes(position)) score += 25;
  if (racket.background.includes(background)) score += 20;

  const powerFit = Math.abs(style - racket.stats.power) / 100;
  score += (1 - powerFit) * 15;

  const combined = racket.stats.power * 0.6 + racket.stats.maneuverability * 0.4;
  const paceFit = Math.abs(pace - combined) / 100;
  score += (1 - paceFit) * 10;

  if (style < racket.powerRange[0]) score -= 20;
  if (style > racket.powerRange[1]) score -= 20;

  return Math.round(score);
}

export function getRecommendations(answers, count = 3) {
  return [...RACKETS]
    .map((r) => ({ ...r, score: scoreRacket(r, answers) }))
    .sort((a, b) => b.score - a.score)
    .slice(0, count);
}

export function getRacketBySlug(slug) {
  return RACKETS.find((r) => r.slug === slug);
}

